<script>
    $(document).ready(function() {
        $('#club_form').submit(function(e) {
            e.preventDefault(); // Prevent default form submission
            var formData = $(this).serialize(); // Serialize form data

            $.ajax({
                type: 'POST',
                url: '<?php echo base_url("ClubTTJ/submit_form"); ?>',
                data: formData,
                dataType: 'json', // Expect a JSON response
                success: function(response) {
                    console.log(response); // Log the response for debugging
                    if (response.status == 1) {
                        var thankYouUrl = 'https://www.ttjtravmart.com/Thanks?MakeID=' + response.MakeID;
                        
                        window.location.href = thankYouUrl;
                    } else {
                        alert(response.message);
                    }
                },
                error: function(xhr, status, error) {
                    console.error(xhr.responseText); // Log error details
                    alert('An error occurred while submitting the form.'); // Notify user
                }
            });
        });
    });
</script>